public class GardenArea {
    public static void main(String[] args ) {
        int length = 50; 
        int width = 30;
        float area = length * width;
        float perimeter = 2 * (length + width);

        System.out.println("The length of the garden is " + width + " meters");
        System.out.println("The width of the garden is " + length + " meters");
        System.out.println("\n\n");
        System.out.println("The area of the garden is " + area + " square meters");
        System.out.println("The perimeter of the garden is " + perimeter + " square meters");

    }
}